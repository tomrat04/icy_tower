def _jump_air_time(vy0: float, levels_up: int) -> float:
    target = levels_up * LEVEL_HEIGHT
    a = 0.5 * GRAVITY
    disc = vy0 * vy0 - 4 * a * target
    if disc <= 0:
        return 0.85
    t1 = (-vy0 - math.sqrt(disc)) / (2 * a)
    t2 = (-vy0 + math.sqrt(disc)) / (2 * a)
    return max(t1, t2)

